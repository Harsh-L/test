package ejb;

/**
 *
 * @author ritesh
 */
public class CartItem {

    int billid;
    int productid;
    int price;
    int quantity;
    int total;
    String itemname;

    public CartItem() {
    }

    public CartItem(int billid, int productid, int price, int quantity, int total, String itemname) {
        this.billid = billid;
        this.productid = productid;
        this.price = price;
        this.quantity = quantity;
        this.total = total;
        this.itemname = itemname;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getItemname() {
        return itemname;
    }

    public void setItemname(String itemname) {
        this.itemname = itemname;
    }

    public int getBillid() {
        return billid;
    }

    public void setBillid(int billid) {
        this.billid = billid;
    }

    public int getProductid() {
        return productid;
    }

    public void setProductid(int productid) {
        this.productid = productid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

}
